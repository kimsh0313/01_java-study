/*
    컬렉션 프레임 워크 Collection Framework
    - 방대한 데이터들을 쉽고 효과적으로 처리할 수 있도록 표준화된 방법을 제공하는 클래스들의 집합
        => 데이터를 효율적으로 저장하는 자료구조와 효율적으로 처리하는 알고리즘이 미리 구현되어 있음

    - 배열과 컬렉션의 차이
    1. 배열
        - 크기에 대한 제약(선언 시, 배열의 크기를 지정 필수)
        - 배열 내 값들 사이에 새로 값을 추가하거나 삭제할 경우 해당 코드를 직접 작성 필요
    2. 컬렉션
        - 크기에 대한 제약이 없음(기본적으로 지정되어있는 크기를 초과하는 경우 자동적으로 크기 변경)
        -  내 값들 사이에 새로 값을 추가하거나 삭제하는 경우에 대한 메서드가 이미 구현되어 있음
    2.1. 컬렉션 인터페이스
        - List 인터페이스
            - 순서가 있는 데이터 집합
            - 순서가 유지되므로 데이터 중복 저장 허용
            - ArrayList, Vector(Deprecated), LinkedList, Stack ...
        - Set 인터페이스
            - 순서가 없는 데이터 집합
            - 데이터의 중복 저장을 허용하지 않음
            - HashSet, TreeSet ...
        - Map 인터페이스
            - Key, Value의 한 쌍으로 이뤄진 데이터 집합
            - Key는 중복을 허용하지 않고 Value는 중복을 허용
            - 종류: HashMap, TreeMap, HashTable, Properties ...
 */


package com.ino.sectio01.math;

public class Application {
    public static void main(String[] args) {
        System.out.println(Math.PI);
        int num1 = -10;
        System.out.println("abs : " + Math.abs(num1));

        double n2 = 4.677;

        System.out.println("ceil : " + Math.ceil(n2));

        System.out.println("round : " + Math.round(n2));

        System.out.println("floor : " + Math.floor(n2));

        System.out.println("rint : " + Math.rint(n2));

        System.out.println("sqrt : " + Math.sqrt(n2));

        System.out.println("pow : " + Math.pow(n2, 3));

    }
}
